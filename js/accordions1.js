


function myFunction(yeh50) {
  var x = document.getElementById(yeh50);
  if (x.className.indexOf("w3-show") == -1) {
    x.className += " w3-show";
  } else {
    x.className = x.className.replace(" w3-show", "w3-hide");
  }
}


